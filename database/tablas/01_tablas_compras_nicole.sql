-- Estructura de tablas para FERREMÁS --
CREATE TABLE PROVEEDORES (
    IDProveedor NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Nombre VARCHAR2(100) NOT NULL,
    Telefono VARCHAR2(20),
    Email VARCHAR2(100),
    Direccion VARCHAR2(200),
    Estado NUMBER(1) DEFAULT 1
);

CREATE TABLE PRODUCTOS (
    IDProducto NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Nombre VARCHAR2(100),
    Descripcion VARCHAR2(200),
    PrecioVenta NUMBER(12,2),
    StockActual NUMBER,
    StockMinimo NUMBER,
    IDProveedor NUMBER,
    CONSTRAINT FK_PROD_PROV FOREIGN KEY (IDProveedor) REFERENCES PROVEEDORES(IDProveedor)
);

CREATE TABLE COMPRAS (
    IDCompra NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Fecha DATE DEFAULT SYSDATE,
    IDProveedor NUMBER,
    TotalCompra NUMBER(12,2) DEFAULT 0,
    CONSTRAINT FK_COMPRA_PROV FOREIGN KEY (IDProveedor) REFERENCES PROVEEDORES(IDProveedor)
);

CREATE TABLE DETALLE_COMPRA (
    IDDetalleCompra NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    IDCompra NUMBER,
    IDProducto NUMBER,
    Cantidad NUMBER,
    PrecioCompra NUMBER(12,2),
    Subtotal NUMBER(12,2),
    CONSTRAINT FK_DETALLE_COMPRA FOREIGN KEY (IDCompra) REFERENCES COMPRAS(IDCompra),
    CONSTRAINT FK_DETALLE_PROD FOREIGN KEY (IDProducto) REFERENCES PRODUCTOS(IDProducto)
);